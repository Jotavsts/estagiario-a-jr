// Declara uma variável chamada "variavel" com valor inicial
let variavel = 10;

// Exibe o valor da variável no console
console.log(variavel);

// Operadores matemáticos básicos:
// + adição, - subtração, * multiplicação, / divisão

// Soma: atribui o resultado de uma adição à variável
variavel = 20 + 20;
console.log(variavel);

// Subtração: atribui o resultado de uma subtração à variável
variavel = 55 - 35;
console.log(variavel);

// Multiplicação: atribui o resultado de uma multiplicação à variável
variavel = 20 * 35;
console.log(variavel);

// Divisão: atribui o resultado de uma divisão à variável
variavel = 35 / 7;
console.log(variavel);
